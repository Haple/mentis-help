            <p class="lead subtopic-content"> A Doença de Alzheimer é um transtorno neurodegenerativo progressivo e
                fatal que se manifesta pela deterioração cognitiva e da memória, comprometimento progressivo das
                atividades de vida diária e uma variedade de sintomas neuropsiquiátricos e de alterações
                comportamentais. </p>
            <p class="lead subtopic-content pb-3"> As causas da doença de Alzheimer são desconhecidas, mas a idade é
                considerada o principal fator para o seu desenvolvimento - após os 65 anos, o risco de desenvolver a
                enfermidade dobra a cada cinco anos. Estudos mostram também que pessoas que possuem familiares com
                Alzheimer têm risco maior de desenvolver a doença. No entanto, isso não quer dizer que ela seja
                hereditária.</p>


            <h4 class="pb-2" id="oque">Sintomas</h4>
            <p class="lead">Entre seus sintomas se encontram:</p>
            <ul class="list-group pb-3">
                <li class="list-group-item border-0">• Falta de memória para acontecimentos recentes</li>
                <li class="list-group-item border-0">• Repetição da mesma pergunta várias vezes</li>
                <li class="list-group-item border-0">• Dificuldade para acompanhar conversações ou pensamentos complexos
                </li>
                <li class="list-group-item border-0">• Incapacidade de elaborar estratégias para resolver problemas</li>
                <li class="list-group-item border-0">• Dificuldade para dirigir automóvel e encontrar caminhos
                    conhecidos</li>
                <li class="list-group-item border-0">• Dificuldade para encontrar palavras que exprimam ideias ou
                    sentimentos pessoais</li>
                <li class="list-group-item border-0">• Irritabilidade, suspeição injustificada, agressividade,
                    passividade, interpretações erradas de estímulos visuais ou auditivos, tendência ao isolamento</li>
            </ul>

